# Run cargo tests when doing cargo run
cargo() {
  if [ "$1" = "run" ]; then
    command cargo test --all-features && command cargo run "${@:2}"
  else
    command cargo "$@"
  fi
}

nvim() {
  if [ "$1" = "gui" ]; then
    neovide "${@:2}" &
    disown
  else
    /usr/bin/nvim "${@}"
  fi
}

# Do a fuzzy find and pipe the output into neovim
fz() {
  nvim "$(fzf --preview 'cat {}')"
}
# Alias for fz
cdf() {
  fz
}

